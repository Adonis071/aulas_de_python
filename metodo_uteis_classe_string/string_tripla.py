"""String Multiplas:
São definidas usando três aspas simples (''') ou trÊs aspas duplas (").
elas permitem escrever textos de multiplas linhas sem a necessidade de \n explicitamente
"""
mensagem = """Olá,
meu nome é Adonis
e eu gosto de Python!"""

print(mensagem)

poema = '''Rosas são vermelhas,
violetas são azuis,
Python é incrível,
e você também.'''

print(poema)
