cursor = "   pYtHon   "

# Todos caracteres para máiusculo:
print(cursor.upper())

# Todos caracteres para minusculo:
print(cursor.lower())

# Fornato para Título:
print(cursor.title())

# Remove espaço da esquerda e da direita
print(cursor.strip())

# Remove espaço da esquerda
print(cursor.lstrip())

# Remove espaço da direita:
print(cursor.rstrip())

# Caracteres ao centro o caracter que foi escolhido:
print(cursor.center(17, "*"))

# Junção de caracteres join():
print(';' .join(cursor))
